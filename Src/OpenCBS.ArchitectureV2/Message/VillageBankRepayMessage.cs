using TinyMessenger;

namespace OpenCBS.ArchitectureV2.Message
{
    public class VillageBankRepayMessage : ITinyMessage
    {
        public VillageBankRepayMessage(object sender, int villageBankId)
        {
            Sender = sender;
            VillageBankId = villageBankId;
        }

        public object Sender { get; private set; }

        public int VillageBankId { get; private set; }
    }
}
